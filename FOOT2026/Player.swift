import Foundation

// MARK: - Position

enum Position: String, CaseIterable, Codable {
    case goalkeeper = "GK"
    case defender   = "DEF"
    case midfielder = "MIL"
    case forward    = "ATT"

    /// Always French — the app's primary language.
    var localizedName: String {
        switch self {
        case .goalkeeper: return "Gardien"
        case .defender:   return "Défenseur"
        case .midfielder: return "Milieu"
        case .forward:    return "Attaquant"
        }
    }
}

// MARK: - Player

struct Player: Identifiable, Codable, Hashable {
    let id: UUID
    var name: String
    var number: Int
    var position: Position
    var team: String

    init(id: UUID = UUID(), name: String, number: Int, position: Position, team: String) {
        self.id = id
        self.name = name
        self.number = number
        self.position = position
        self.team = team
    }
}

